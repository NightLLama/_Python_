alien = {"color": "verde", "puntos": 5}
print(alien["color"])
print(alien["puntos"])

# un diccionario es una estructura dinamica a la que se le puede añadir cosas
alien["x_position"] = 0
alien["y_position"] = 25
print(alien)

# también podemos usar un diccionario vacío
aliend = {}
aliend["clave"] = "valor"
print(aliend)
